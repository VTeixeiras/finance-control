    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        String nome = txtNome.getText().toUpperCase();
        String tel = txtTelefone.getText().toUpperCase();
        String endereco = txtEndereco.getText().toUpperCase();

        //Validações de campo
        if (nome.trim().equals("")) {
            Alerta.msg("Aviso...", "O campo NOME não pode ficar em branco!", JOptionPane.WARNING_MESSAGE);

        } else if (endereco.trim().equals("")) {
            Alerta.msg("Aviso...", "O campo ENDEREÇO não pode ficar em branco!", JOptionPane.WARNING_MESSAGE);

        } else if (tel.equals("(  )     -    )")) {
            Alerta.msg("Aviso...", "O campo TELEFONE está inválido!", JOptionPane.WARNING_MESSAGE);

        } else {
            
            ModelFornecedores m = new ModelFornecedores();
            m.setNome(nome);
            m.setEndereco(endereco);
            m.setTelefone(tel);            
            
            ControlFornecedores c = new ControlFornecedores();
            
            if (tipo == 1) {
                //Novo Fornecedor
                c.insere(m);
                
            } else if (tipo == 2) {
                //Altera Fornecedor
                m.setCod(codFornecedor);
                c.altera(m);
            }

        }


    }//GEN-LAST:event_btnSalvarActionPerformed

