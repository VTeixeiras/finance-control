    private void criaTabela() {
        tbNotas.setModel(modelo);
        tbNotas.getColumnModel().getColumn(2).setPreferredWidth(150);
        tbNotas.getColumnModel().getColumn(3).setPreferredWidth(300);
    }

    private void preencheTabela() {
        modelo.setNumRows(0);

        ControlNotas c = new ControlNotas();
        rs = c.listaNotas();

        try {

            while (rs.next()) {
                modelo.addRow(new Object[]{rs.getObject("id"), rs.getObject("num_nota"), rs.getObject("descricao"), 
                                           rs.getObject("data_venc"), rs.getObject("valor")});
            }

        } catch (SQLException ex) {
            Alerta.msg("Erro...", "Erro de SQL! \nLocal: ViewNotas \nErro: " + ex.getMessage(),
                    JOptionPane.ERROR_MESSAGE);

        }
    }
    
